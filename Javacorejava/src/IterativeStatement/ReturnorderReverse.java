public class ReturnorderReverse {
	
	public static void main(String[] args) {
	//difference = 2
		int a = 20;
		while(a>=2) {
			System.out.println(a);
			 a=a-2;
		}               //output = 20,18,16,......4,2
	
	//    difference =1
	
		int i = 12;
	    while(i>=1) {
		System.out.print(i);
		i--;    }
			}
}                 //output = 12,11,10,9,......1
