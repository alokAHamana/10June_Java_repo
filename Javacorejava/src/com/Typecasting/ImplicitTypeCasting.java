package com.Typecasting;

public class ImplicitTypeCasting {   //upcasting    Automatic compiler karta h

	public static void main(String[] args) {
		
	int a= 10;
	double d = a;
	                   
	System.out.println(d);    /*int value 10 automatic double
	                           mein convert ho jayega
	                               ans =10.0       */
	
	char c = 'a';
	int b = c ;
	System.out.println(b);      //97 char val - int mein convert ho gai
	
	
	}
}
