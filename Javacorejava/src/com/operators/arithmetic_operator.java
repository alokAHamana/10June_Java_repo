public class arithmetic_operator {
     
	//plus operators
	
	public static void main(String[] args) {
		
		int a=10;
		int b=30;
		int c=40;
		String d= "ALOK";		
				
	System.out.println(a+b+c+d);    //80ALOK
	
	System.out.println(a+d+c+b);   //10ALOK70
		
	System.out.println(d+a+b+c);   //ALOK80
	
	
	}
}
