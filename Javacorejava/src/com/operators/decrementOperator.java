package com.operators;

public class decrementOperator {

	public static void main(String[] args) {
		
		//pre dec

	int x=4;
	int y=--x;
	
	System.out.println(y);     //3
	System.out.println(x);     //3
	//post dec
	
	
	int a= 6;
	int b= a--;
	
	System.out.println(b);     //6
	System.out.println(a);      //5
	
	
	}
}
